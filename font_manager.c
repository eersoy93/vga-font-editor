void InitializeVGAFont(VGAFont* font)
{
    if (!font) return;
    
    // Clear all character data
    memset(font->data, 0, sizeof(font->data));
}

void LoadBasicASCIIChars(VGAFont* font)
{
    if (!font) return;
    
    // Load basic ASCII characters (32-126)
    for (int i = 0; i < 95 && i < (int)(sizeof(basic_ascii_font) / VGA_CHAR_HEIGHT); i++) {
        memcpy(font->data[32 + i], basic_ascii_font[i], VGA_CHAR_HEIGHT);
    }
    
    // Create a simple block character for character 219 (█)
    memset(font->data[219], 0xFF, VGA_CHAR_HEIGHT);
    
    // Create a checkerboard pattern for character 177 (▒)
    for (int y = 0; y < VGA_CHAR_HEIGHT; y++) {
        font->data[177][y] = (y % 2) ? 0xAA : 0x55;
    }
    
    // Create a light shade for character 176 (░)
    for (int y = 0; y < VGA_CHAR_HEIGHT; y++) {
        font->data[176][y] = (y % 4 < 2) ? 0x88 : 0x22;
    }
    
    // Create a dark shade for character 178 (▓)
    for (int y = 0; y < VGA_CHAR_HEIGHT; y++) {
        font->data[178][y] = (y % 2) ? 0xDD : 0x77;
    }
}

BOOL LoadVGAFont(VGAFont* font, const char* filename)
{
    if (!font || !filename) return FALSE;
    
    FILE* file = fopen(filename, "rb");
    if (!file) return FALSE;
    
    // Try to read the font data
    size_t bytesRead = fread(font->data, 1, sizeof(font->data), file);
    fclose(file);
    
    // Check if we read the expected amount of data
    if (bytesRead != sizeof(font->data)) {
        // If the file is smaller, it might be a different format
        // For now, just check if we got at least some data
        if (bytesRead < VGA_FONT_CHARS * VGA_CHAR_HEIGHT) {
            return FALSE;
        }
    }
    
    return TRUE;
}

BOOL SaveVGAFont(VGAFont* font, const char* filename)
{
    if (!font || !filename) return FALSE;
    
    FILE* file = fopen(filename, "wb");
    if (!file) return FALSE;
    
    // Write the font data
    size_t bytesWritten = fwrite(font->data, 1, sizeof(font->data), file);
    fclose(file);
    
    return (bytesWritten == sizeof(font->data));
}

void SetFontPixel(VGAFont* font, int charIndex, int x, int y, BOOL value)
{
    if (!font || charIndex < 0 || charIndex >= VGA_FONT_CHARS) return;
    if (x < 0 || x >= VGA_CHAR_WIDTH || y < 0 || y >= VGA_CHAR_HEIGHT) return;
    
    unsigned char mask = 1 << (7 - x);  // MSB is leftmost pixel
    
    if (value) {
        font->data[charIndex][y] |= mask;
    } else {
        font->data[charIndex][y] &= ~mask;
    }
}

